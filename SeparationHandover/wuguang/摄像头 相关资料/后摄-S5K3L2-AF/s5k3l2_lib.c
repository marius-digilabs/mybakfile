/*===========================================================================
 * FUNCTION    - s5k3l2_real_to_register_gain -
 *
 * DESCRIPTION:
 *==========================================================================*/
static uint16_t s5k3l2_real_to_register_gain(float gain)
{
  uint16_t reg_gain;

  if(gain < 1.0) {
      gain = 1.0;
  } else if (gain > 16.0) {
      gain = 16.0;
  }
  gain = (gain) * 32.0;
  reg_gain = (uint16_t) gain;

  return reg_gain;
}

/*===========================================================================
 * FUNCTION    - s5k3l2_register_to_real_gain -
 *
 * DESCRIPTION:
 *==========================================================================*/
static float s5k3l2_register_to_real_gain(uint16_t reg_gain)
{
  float real_gain;

  if (reg_gain > 0x200) {
      reg_gain = 0x200;
  }
  real_gain = (float) reg_gain / 32.0;

  return real_gain;
}

/*===========================================================================
 * FUNCTION    - s5k3l2_calculate_exposure -
 *
 * DESCRIPTION:
 *==========================================================================*/
static int32_t s5k3l2_calculate_exposure(float real_gain,
  uint16_t line_count, sensor_exposure_info_t *exp_info)
{
  if (!exp_info) {
    return -1;
  }
  exp_info->reg_gain = s5k3l2_real_to_register_gain(real_gain);
  exp_info->sensor_real_gain = s5k3l2_register_to_real_gain(exp_info->reg_gain);
  exp_info->digital_gain = real_gain / exp_info->sensor_real_gain;
  exp_info->line_count = line_count;
  exp_info->sensor_digital_gain = 0x1;
  return 0;
}

/*===========================================================================
 * FUNCTION    - s5k3l2_fill_exposure_array -
 *
 * DESCRIPTION:
 *==========================================================================*/
static int32_t s5k3l2_fill_exposure_array(uint16_t gain, uint32_t line,
  uint32_t fl_lines, int32_t luma_avg, uint32_t fgain,
  struct msm_camera_i2c_reg_setting *reg_setting)
{
  int32_t rc = 0;

  uint16_t reg_count = 0;
  uint16_t i = 0;

  if (!reg_setting) {
    return -1;
  }

  for (i = 0; i < sensor_lib_ptr.groupon_settings->size; i++) {
    reg_setting->reg_setting[reg_count].reg_addr =
      sensor_lib_ptr.groupon_settings->reg_setting[i].reg_addr;
    reg_setting->reg_setting[reg_count].reg_data =
      sensor_lib_ptr.groupon_settings->reg_setting[i].reg_data;
    reg_count = reg_count + 1;
  }

  reg_setting->reg_setting[reg_count].reg_addr = 0x602A;
  reg_setting->reg_setting[reg_count].reg_data =
    (sensor_lib_ptr.output_reg_addr->frame_length_lines & 0xFF00) >> 8;
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x602B;
  reg_setting->reg_setting[reg_count].reg_data =
    (sensor_lib_ptr.output_reg_addr->frame_length_lines & 0xFF);
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x6F12;
  reg_setting->reg_setting[reg_count].reg_data = (fl_lines & 0xFF00) >> 8;
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x6F12;
  reg_setting->reg_setting[reg_count].reg_data = (fl_lines & 0xFF);
  reg_count++;

  reg_setting->reg_setting[reg_count].reg_addr = 0x602A;
  reg_setting->reg_setting[reg_count].reg_data =
    (sensor_lib_ptr.exp_gain_info->coarse_int_time_addr & 0xFF00) >> 8;
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x602B;
  reg_setting->reg_setting[reg_count].reg_data =
    (sensor_lib_ptr.exp_gain_info->coarse_int_time_addr & 0xFF);
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x6F12;
  reg_setting->reg_setting[reg_count].reg_data = (line & 0xFF00) >> 8;
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x6F12;
  reg_setting->reg_setting[reg_count].reg_data = (line & 0xFF);
  reg_count++;

  reg_setting->reg_setting[reg_count].reg_addr = 0x602A;
  reg_setting->reg_setting[reg_count].reg_data =
    (sensor_lib_ptr.exp_gain_info->global_gain_addr & 0xFF00) >> 8;
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x602B;
  reg_setting->reg_setting[reg_count].reg_data =
    (sensor_lib_ptr.exp_gain_info->global_gain_addr & 0xFF);
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x6F12;
  reg_setting->reg_setting[reg_count].reg_data = (gain & 0xFF00) >> 8;
  reg_count++;
  reg_setting->reg_setting[reg_count].reg_addr = 0x6F12;
  reg_setting->reg_setting[reg_count].reg_data = (gain & 0xFF);
  reg_count++;

  for (i = 0; i < sensor_lib_ptr.groupoff_settings->size; i++) {
    reg_setting->reg_setting[reg_count].reg_addr =
      sensor_lib_ptr.groupoff_settings->reg_setting[i].reg_addr;
    reg_setting->reg_setting[reg_count].reg_data =
      sensor_lib_ptr.groupoff_settings->reg_setting[i].reg_data;
    reg_count = reg_count + 1;
  }

  reg_setting->size = reg_count;
  reg_setting->addr_type = MSM_CAMERA_I2C_WORD_ADDR;
  reg_setting->data_type = MSM_CAMERA_I2C_BYTE_DATA;
  reg_setting->delay = 0;

  return rc;
}

